// comparasion operators are used to compare two values and 
// return a boolean result (true or false)

console.log(5 > 3); // true
console.log(5 < 3); // false
console.log(5 >= 3); // true
console.log(5 <= 3); // false           
console.log(5 == 5); // true
console.log(5 == '5'); // true (loose equality, checks value only)
console.log(5 === '5'); // false (strict equality, checks value and type)
console.log(5 != 3); // true
console.log(5 != 5); // false
console.log(5 !== '5'); // true (strict inequality, checks value and type)  
console.log(5 > 3 && 5 < 10); // true (logical AND)
console.log(5 > 3 || 5 < 2); // true (logical OR)
console.log(!(5 > 3)); // false (logical NOT)   